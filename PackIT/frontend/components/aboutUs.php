  <script>
    (function() {
      // 1. Counter Logic
      function runCounters(container) {
        const counters = container.querySelectorAll('[data-counter]');
        counters.forEach(el => {
          const raw = el.getAttribute('data-counter');
          const isPlus = String(raw).trim().startsWith('+');
          const numeric = parseInt(String(raw).replace(/\D/g, ''), 10) || 0;
          let start = 0;
          const duration = 1000;
          const stepTime = 20;
          const stepAmount = Math.ceil(numeric / (duration / stepTime));

          el.textContent = isPlus ? '0+' : '0';

          const timer = setInterval(() => {
            start += stepAmount;
            if (start >= numeric) {
              start = numeric;
              clearInterval(timer);
            }
            if (isPlus) el.textContent = start + '+';
            else if (numeric === 24 && raw.includes('24/7')) el.textContent = (start === 24 ? '24/7' : start);
            else el.textContent = start;
          }, stepTime);
        });
      }

      // 2. Animation Logic
      const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            const el = entry.target;
            const delay = parseInt(el.getAttribute('data-delay') || '0', 10);
            
            setTimeout(() => {
              el.style.opacity = '1';
              el.style.transform = 'none';
            }, delay);

            if (el.querySelectorAll('[data-counter]').length > 0) {
              runCounters(el);
            }

            observer.unobserve(el);
          }
        });
      }, { threshold: 0.15 });

      document.querySelectorAll('[data-animate]').forEach(el => {
        observer.observe(el);
      });
    })();
  </script>
